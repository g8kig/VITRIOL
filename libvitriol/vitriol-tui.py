# ── Paths ──────────────────────────────────────────────────────

CONFIG_DIR = Path(os.environ.get("HOME", "~")) / ".vitriol"
CONFIG_PATH = CONFIG_DIR / "config"
LOG_PATH = CONFIG_DIR / "server.log"
PID_PATH = CONFIG_DIR / "server.pid"
LOGO_PATH = Path(__file__).resolve().parent.parent / "assets" / "ansi-logo.txt"
SCRIPT_PATH = Path(__file__).resolve().parent.parent / "scripts" / "vitriol"

# ── Helpers ────────────────────────────────────────────────────

def get_vram() -> tuple[float, float]:
    """Return (used_mib, total_mib) via nvidia-smi."""
    try:
        out = subprocess.check_output(
            ["nvidia-smi", "--query-gpu=memory.used,memory.total",
             "--format=csv,noheader,nounits"],
            timeout=3, text=True
        )
        parts = out.strip().split(", ")
        return float(parts[0]), float(parts[1])
    except Exception:
        return 0.0, 0.0

def load_logo() -> str:
    """Return ANSI logo as a string, or empty if not found."""
    if not LOGO_PATH.exists():
        return ""
    with open(LOGO_PATH) as f:
        lines = f.readlines()
        for line in lines:
            if "echo -e" in line or r"\033" in line:
                idx = line.find(r"\033")
                if idx >= 0:
                    return line[idx:].strip().rstrip("'").replace(r"\033", "\033").replace("'", "")
        return ""

def load_config() -> dict:
    """Parse ~/.vitriol/config into a flat dict with section.key."""
    cfg = {}
    if not CONFIG_PATH.exists():
        return cfg
    parser = configparser.ConfigParser()
    parser.read(CONFIG_PATH)
    for section in parser.sections():
        for key, val in parser[section].items():
            cfg[f"{section}.{key}"] = val
    return cfg

def save_config(flat: dict) -> None:
    """Write flat dict back to ~/.vitriol/config, preserving section structure."""
    sections = {}
    for full_key, val in flat.items():
        if "." in full_key:
            sec, key = full_key.split(".", 1)
        else:
            sec, key = "vitriol", full_key
        sections.setdefault(sec, {})[key] = val
    CONFIG_DIR.mkdir(parents=True, exist_ok=True)
    parser = configparser.ConfigParser()
    # Preserve existing order by reading first
    if CONFIG_PATH.exists():
        parser.read(CONFIG_PATH)
    # Override values
    for sec, kv in sections.items():
        if sec not in parser:
            parser[sec] = {}
        for k, v in kv.items():
            parser[sec][k] = str(v)
    with open(CONFIG_PATH, "w") as f:
        f.write("# VITRIOL Configuration\n")
        f.write("# Generated by vitriol tui\n\n")
        parser.write(f)

def is_server_running() -> bool:
    """Check if llama-server PID is alive."""
    if not PID_PATH.exists():
        return False
    try:
        pid = int(PID_PATH.read_text().strip())
        os.kill(pid, 0)
        return True
    except (ValueError, OSError, FileNotFoundError):
        return False

def stop_server() -> bool:
    """Stop running server via PID file. Returns True if stopped."""
    if not PID_PATH.exists():
        return False
    try:
        pid = int(PID_PATH.read_text().strip())
        os.kill(pid, signal.SIGTERM)
        # Wait briefly
        for _ in range(10):
            try:
                os.kill(pid, 0)
                time.sleep(0.5)
            except OSError:
                break
        PID_PATH.unlink(missing_ok=True)
        return True
    except (ValueError, OSError, FileNotFoundError):
        return False


# ── Log Parser ──────────────────────────────────────────────────

TIMING_RE = re.compile(
    r"eval time\s*=\s*([\d.]+)\s*ms\s*/\s*(\d+)\s+tokens"
)
PROMPT_RE = re.compile(
    r"prompt eval time\s*=\s*([\d.]+)\s*ms\s*/\s*(\d+)\s+tokens"
)
DRAFT_RE = re.compile(
    r"draft acceptance rate\s*=\s*([\d.]+)"
)

class ServerLogWatcher:
    """Watches server log file for new timing lines."""

    def __init__(self):
        self._pos = LOG_PATH.stat().st_size if LOG_PATH.exists() else 0
        self.last_tps: float = 0.0
        self.last_prompt_tps: float = 0.0
        self.last_acceptance: float = 0.0
        self.new_data_available = False

    def poll(self) -> None:
        if not LOG_PATH.exists():
            return
        try:
            with open(LOG_PATH, errors="replace") as f:
                f.seek(self._pos)
                new_data = f.read()
                self._pos = f.tell()

            lines = new_data.splitlines()
            if not lines:
                return

            self.new_data_available = False
            for line in lines:
                m = TIMING_RE.search(line)
                if m:
                    ms, tokens = float(m.group(1)), int(m.group(2))
                    self.last_tps = (tokens / ms) * 1000 if ms > 0 else 0.0
                    self.new_data_available = True
                m = PROMPT_RE.search(line)
                if m:
                    ms, tokens = float(m.group(1)), int(m.group(2))
                    self.last_prompt_tps = (tokens / ms) * 1000 if ms > 0 else 0.0
                    self.new_data_available = True
                m = DRAFT_RE.search(line)
                if m:
                    self.last_acceptance = float(m.group(1))
                    self.new_data_available = True
        except (IOError, PermissionError):
            pass


