class ConfigScreen(Screen):
    """Full-screen config editor with labeled inputs."""

    BINDINGS = [("escape", "back", "Back to Dashboard")]

    def compose(self):
        yield Header()
        with ScrollableContainer():
            yield Label("VITRIOL Configuration", classes="section-title")
            yield Label("Edit values and press Save. Changes take effect on next server start.")
            self.inputs = {}
            cfg = load_config()
            for section in ["model", "vitriol", "spec", "kv", "server"]:
                yield Label(f"[{section}]", classes="section-header")
                for key, label, hint in CONFIG_FIELDS:
                    if not key.startswith(f"{section}."):
                        continue
                    k = key.split(".", 1)[1]
                    val = cfg.get(key, "")
                    yield Label(label, classes="field-label")
                    inp = Input(
                        value=val,
                        placeholder=hint,
                        id=f"cfg-{section}-{k}",
                        classes="field-input"
                    )
                    self.inputs[key] = inp
                    yield inp
        with Horizontal(classes="button-row"):
            yield Button("Save & Apply", id="save-config", variant="primary")
            yield Button("Discard", id="discard-config", variant="default")

    def on_button_pressed(self, event: Button.Pressed):
        if event.button.id == "save-config":
            new_cfg = {}
            for key, inp in self.inputs.items():
                new_cfg[key] = inp.value
            save_config(new_cfg)
            self.app.pop_screen()
            self.app.query_one("#status-msg", Static).update("Config saved")
        elif event.button.id == "discard-config":
            self.app.pop_screen()

    def action_back(self):
        self.app.pop_screen()


# ── Server Launch Modal ────────────────────────────────────────

class ServerLaunchScreen(ModalScreen):
    """Modal showing server launch progress."""

    def compose(self):
        yield Vertical(
            Label("Starting VITRIOL server...", classes="section-title"),
            LoadingIndicator(),
            Static("Loading model, please wait (30-60s)", id="launch-status"),
            Button("Close", id="close-launch", variant="default"),
        )

    def on_mount(self):
        self.launch()

    @work(thread=True)
    def launch(self):
        try:
            result = subprocess.run(
                ["bash", str(SCRIPT_PATH), "serve", "--detach", "--kv-mode", "offload"],
                capture_output=True, text=True, timeout=300
            )
            self.call_from_thread(self._update_status, result.returncode == 0)
        except subprocess.TimeoutExpired:
            self.call_from_thread(self._update_status, False, "Timeout")
        except Exception as e:
            self.call_from_thread(self._update_status, False, str(e))

    def _update_status(self, success: bool, msg: str = ""):
        if success:
            self.query_one("#launch-status", Static).update("✓ Server started successfully")
        else:
            self.query_one("#launch-status", Static).update(f"✗ Failed: {msg or 'check logs'}")

    def on_button_pressed(self, event: Button.Pressed):
        self.app.pop_screen()


