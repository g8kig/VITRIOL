    def compose(self):
        with TabbedContent(initial="tab-dash"):
            # ── Dashboard Tab ──
            with TabPane("Dashboard", id="tab-dash"):
                with Horizontal():
                    # Logo + Status column
                    with Vertical(id="left-col"):
                        yield Static(load_logo() or "VITRIOL", id="logo")
                        yield Static(id="status-msg")
                    # Stats column
                    with Vertical(id="center-col"):
                        yield Label("Live Stats", classes="section-title")
                        yield Static("Gen: --  t/s", id="stat-tps")
                        yield Static("Prompt: -- t/s", id="stat-ptps")
                        yield Static("MTP: --", id="stat-mtp")
                        yield Static("VRAM: --", id="stat-vram")
                        yield Static("Server: --", id="stat-server")
                # Bottleneck chart (placeholder)
                yield Static(
                    "Layer   FFN         Attn        PCIe\n"
                    " 0-10   ████████   ████       ████████\n"
                    "11-20   ████████   ████       ████████\n"
                    "21-30   ████████   ████       ░░░ (pin)\n"
                    "31-39   ████████   ████       ░░░ (pin)\n",
                    id="bottleneck"
                )
                # Log
                yield RichLog(id="log", highlight=True, max_lines=50)

            # ── Config Tab ──
            with TabPane("Config", id="tab-config"):
                with ScrollableContainer():
                    yield Label("VITRIOL Configuration", classes="section-title")
                    self.config_inputs = {}
                    cfg = load_config()
                    for section in ["model", "vitriol", "spec", "kv", "server"]:
                        yield Label(f"[{section}]", classes="section-header")
                        for key, label, hint in CONFIG_FIELDS:
                            if not key.startswith(f"{section}."):
                                continue
                            k = key.split(".", 1)[1]
                            val = cfg.get(key, "")
                            yield Label(label + ":", classes="field-label")
                            inp = Input(
                                value=val,
                                placeholder=hint,
                                id=f"cfg-{section}-{k}",
                                classes="field-input"
                            )
                            self.config_inputs[key] = inp
                            yield inp
                with Horizontal(classes="button-row"):
                    yield Button("Save Config", id="save-config", variant="primary")

        with Horizontal(classes="button-row"):
            yield Button("▶ Launch Server", id="btn-launch", variant="success")
            yield Button("■ Stop Server", id="btn-stop", variant="error")
            yield Button("⚙ Config", id="btn-config", variant="default")
            yield Button("Q Quit", id="btn-quit", variant="default")

    def on_mount(self):
        self.watcher = ServerLogWatcher()
        self.poll_loop()

    @work(thread=True, exit_if_already_running=True)
    def poll_loop(self):
        while True:
            self.call_from_thread(self._update)
            time.sleep(1)

    def _update(self):
        # Update VRAM
        used, total = get_vram()
        try:
            vram_w = self.query_one("#stat-vram")
            pct = int((used / total) * 100) if total > 0 else 0
            bar = "█" * (pct // 5) + "░" * (20 - pct // 5)
            vram_w.update(f"VRAM: {bar} {used:.0f}/{total:.0f} MiB ({pct}%)")
        except NoMatches:
            pass

        # Server status
        running = is_server_running()
        try:
            srv_w = self.query_one("#stat-server")
            srv_w.update(f"Server: {'▶ Running' if running else '■ Stopped'}")
        except NoMatches:
            pass

        # Update log watcher
        self.watcher.poll()

        # Update stats
        if self.watcher.last_tps > 0:
            try:
                self.query_one("#stat-tps").update(f"Gen: {self.watcher.last_tps:.1f} t/s")
            except NoMatches:
                pass
        if self.watcher.last_prompt_tps > 0:
            try:
                self.query_one("#stat-ptps").update(f"Prompt: {self.watcher.last_prompt_tps:.1f} t/s")
            except NoMatches:
                pass
        if self.watcher.last_acceptance > 0:
            try:
                self.query_one("#stat-mtp").update(f"MTP: {self.watcher.last_acceptance*100:.1f}%")
            except NoMatches:
                pass

        # Update log
        if self.watcher.new_data_available:
            try:
                log = self.query_one("#log")
                now = time.strftime("%H:%M:%S")
                if self.watcher.last_tps > 0:
                    log.write(f"[{now}] Gen: {self.watcher.last_tps:.1f} t/s")
                if self.watcher.last_acceptance > 0:
                    log.write(f"[{now}] MTP: {self.watcher.last_acceptance*100:.1f}% acc")
            except NoMatches:
                pass

    def on_button_pressed(self, event: Button.Pressed):
        bid = event.button.id
        if bid == "btn-launch":
            self.launch_server()
        elif bid == "btn-stop":
            self.stop_server()
        elif bid == "btn-config":
            self.action_config()
        elif bid == "btn-quit":
            self.app.exit()
        elif bid == "save-config":
            self._save_config()

    def _save_config(self):
        new_cfg = {}
        for key, inp in self.config_inputs.items():
            new_cfg[key] = inp.value
        save_config(new_cfg)
        try:
            self.query_one("#status-msg", Static).update("✓ Config saved")
        except NoMatches:
            pass

    def launch_server(self):
        if is_server_running():
            try:
                self.query_one("#status-msg", Static).update("Server already running")
            except NoMatches:
                pass
            return
        try:
            self.query_one("#status-msg", Static).update("Starting server...")
        except NoMatches:
            pass
        self._do_launch()

    @work(thread=True)
    def _do_launch(self):
        try:
            result = subprocess.run(
                ["bash", str(SCRIPT_PATH), "serve", "--detach"],
                capture_output=True, text=True, timeout=300
            )
            success = result.returncode == 0
            self.call_from_thread(self._launch_result, success,
                                  result.stderr.strip() or result.stdout.strip())
        except Exception as e:
            self.call_from_thread(self._launch_result, False, str(e))

    def _launch_result(self, success: bool, msg: str = ""):
        try:
            w = self.query_one("#status-msg", Static)
            if success:
                w.update("✓ Server started")
            else:
                w.update(f"✗ Launch failed: {msg[:60]}")
        except NoMatches:
            pass

    def stop_server(self):
        if stop_server():
            try:
                self.query_one("#status-msg", Static).update("✓ Server stopped")
                self.query_one("#log", RichLog).write(f"[{time.strftime('%H:%M:%S')}] Server stopped")
            except NoMatches:
                pass

    def action_config(self):
        self.push_screen(ConfigScreen())

    def action_launch(self):
        self.launch_server()

    def action_stop(self):
        self.stop_server()


